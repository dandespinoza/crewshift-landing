// ── Embeddings ──────────────────────────────────────────────────────────────

/**
 * Request payload for the `/embed` AI endpoint.
 * Generates vector embeddings for a batch of texts.
 */
export interface EmbedRequest {
  /** Array of text strings to embed. */
  texts: string[];
  /** Optional model override (defaults to the service's configured model). */
  model?: string;
  /** UUID of the requesting organization. */
  org_id: string;
}

/**
 * Response payload returned by the `/embed` AI endpoint.
 */
export interface EmbedResponse {
  /** Array of embedding vectors, one per input text. */
  embeddings: number[][];
  /** Model identifier that served the request. */
  model_used: string;
  /** Dimensionality of each embedding vector. */
  dimensions: number;
}

// ── Semantic search ─────────────────────────────────────────────────────────

/** A single result from a semantic search query. */
export interface SearchResult {
  /** The type of source document (e.g. "job", "invoice", "note"). */
  source_type: string;
  /** UUID of the source record. */
  source_id: string;
  /** The matched text content. */
  content: string;
  /** Cosine similarity score (0-1). */
  score: number;
  /** Additional metadata about the matched document. */
  metadata: Record<string, unknown>;
}

/**
 * Request payload for the `/search` AI endpoint.
 * Performs semantic (vector) search across an org's indexed data.
 */
export interface SearchRequest {
  /** Natural-language search query. */
  query: string;
  /** UUID of the requesting organization. */
  org_id: string;
  /** Optional filter to restrict which source types are searched. */
  source_types?: string[];
  /** Maximum number of results to return. */
  limit?: number;
}

/**
 * Response payload returned by the `/search` AI endpoint.
 */
export interface SearchResponse {
  /** Ranked list of matching documents. */
  results: SearchResult[];
}
